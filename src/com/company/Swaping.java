package com.company;

public class Swaping {
    public static void main(String[] args){
        double a,b;
        a=5;
        b=4;
        System.out.println("Before swapping: a=: "+a+" b=: "+b);
        double temp;
        temp=a;
        a=b;
        b=temp;
        System.out.println("after swapping: a=: "+a+" b=: "+b);
    }

}
