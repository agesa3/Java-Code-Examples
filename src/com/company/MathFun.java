package com.company;

//mathematical expressions

public class MathFun {
    public static void main(String[] args){
        System.out.println(Math.PI);
        System.out.println(Math.sqrt(25)); //square root of a number
        System.out.println(Math.pow(2,2));
        System.out.println(Math.pow(4,2)); //squareof a number
        System.out.println(Math.cos(25)); //cosine of a number
        System.out.println(Math.log(45));//logarithm of a number
        System.out.println(Math.log10(1000)); //log to base 10 of a number
        System.out.println(Math.exp(34)); //exponential of number
    }
}
